    public void mostrar(){
        JasperReport reporte;
        Conexion obj = new Conexion();
        Map parametros = new HashMap();
        
        parametros.put("nameProyect", cmbMateriaP.getSelectedItem().toString());
        obj.conectar();
          
         try{
            reporte = JasperCompileManager.compileReport("src/com/reportes/reporteMateriaPrima.jrxml");
            JasperPrint jp = JasperFillManager.fillReport(reporte, parametros, obj.getCon());
            JasperViewer.viewReport(jp, true);
         }
         catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Ocurrio este problema: "+e);
             
        }
     
    }
